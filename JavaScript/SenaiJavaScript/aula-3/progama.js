// Igualdade ampla (==)
// Verifica se os valores são iguais, realizando conversões de tipo se necessário.
console.log(5 == 5); // true
console.log(5 == '5'); // true (a string '5' é convertida para o número 5)

// Desigualdade ampla (!=)
// Verifica se os valores são diferentes, realizando conversões de tipo se necessário.
console.log(5 != 5); // false
console.log(5 != '5'); // false

// Igualdade estrita (===)
// Verifica se os valores são iguais e do mesmo tipo.
console.log(5 === 5); // true
console.log(5 === '5'); // false (tipos diferentes)

// Desigualdade estrita (!==)
// Verifica se os valores são diferentes ou de tipos diferentes.
console.log(5 !== 5); // false
console.log(5 !== '5'); // true

// Maior que (>)
// Verifica se o valor à esquerda é maior que o valor à direita.
console.log(5 > 3); // true

// Menor que (<)
// Verifica se o valor à esquerda é menor que o valor à direita.
console.log(3 < 5); // true

// Maior ou igual que (>=)
// Verifica se o valor à esquerda é maior ou igual ao valor à direita.
console.log(5 >= 5); // true
console.log(5 >= 3); // true

// Menor ou igual que (<=)
// Verifica se o valor à esquerda é menor ou igual ao valor à direita.
console.log(3 <= 5); // true
console.log(5 <= 5); // true

